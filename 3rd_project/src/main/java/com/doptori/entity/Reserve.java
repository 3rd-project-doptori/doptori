package com.doptori.entity;

public class Reserve {

	private	int re_num;
	private	int re_mb_nums;
	private	int re_mb_numb;
	private	String re_date;
	private	String re_time;
	private	String re_name;
	private	double re_kg;
	private	int re_price;
	
	
	// getter, setter 만들기
	public int getRe_num() {
		return re_num;
	}
	public void setRe_num(int re_num) {
		this.re_num = re_num;
	}
	public int getRe_mb_nums() {
		return re_mb_nums;
	}
	public void setRe_mb_nums(int re_mb_nums) {
		this.re_mb_nums = re_mb_nums;
	}
	public int getRe_mb_numb() {
		return re_mb_numb;
	}
	public void setRe_mb_numb(int re_mb_numb) {
		this.re_mb_numb = re_mb_numb;
	}
	public String getRe_date() {
		return re_date;
	}
	public void setRe_date(String re_date) {
		this.re_date = re_date;
	}
	public String getRe_time() {
		return re_time;
	}
	public void setRe_time(String re_time) {
		this.re_time = re_time;
	}
	public String getRe_name() {
		return re_name;
	}
	public void setRe_name(String re_name) {
		this.re_name = re_name;
	}
	public double getRe_kg() {
		return re_kg;
	}
	public void setRe_kg(double re_kg) {
		this.re_kg = re_kg;
	}
	public int getRe_price() {
		return re_price;
	}
	public void setRe_price(int re_price) {
		this.re_price = re_price;
	}
	
	
}
