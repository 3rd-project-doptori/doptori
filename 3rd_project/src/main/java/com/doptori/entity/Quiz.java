package com.doptori.entity;

public class Quiz {
	
	private	int qz_num;
	private	String qz_question;
	private	String qz_answer;

	// getter, setter 만들기
	
	public int getQz_num() {
		return qz_num;
	}
	public void setQz_num(int qz_num) {
		this.qz_num = qz_num;
	}
	public String getQz_question() {
		return qz_question;
	}
	public void setQz_question(String qz_question) {
		this.qz_question = qz_question;
	}
	public String getQz_answer() {
		return qz_answer;
	}
	public void setQz_answer(String qz_answer) {
		this.qz_answer = qz_answer;
	}
	
}
