package com.doptori.entity;

public class Crop {
	
	private	int cp_num;
	private String cp_info_num;
	private String cp_item;
	private String cp_kind;
	private	int cp_type;
	private	String cp_title;
	private	String cp_cont;
	
	
	public String getCp_info_num() {
		return cp_info_num;
	}
	public void setCp_info_num(String cp_info_num) {
		this.cp_info_num = cp_info_num;
	}
	// getter, setter 만들기
	public int getCp_num() {
		return cp_num;
	}
	public void setCp_num(int cp_num) {
		this.cp_num = cp_num;
	}
	public String getCp_item() {
		return cp_item;
	}
	public void setCp_item(String cp_item) {
		this.cp_item = cp_item;
	}
	public String getCp_kind() {
		return cp_kind;
	}
	public void setCp_kind(String cp_kind) {
		this.cp_kind = cp_kind;
	}
	public int getCp_type() {
		return cp_type;
	}
	public void setCp_type(int cp_type) {
		this.cp_type = cp_type;
	}
	public String getCp_title() {
		return cp_title;
	}
	public void setCp_title(String cp_title) {
		this.cp_title = cp_title;
	}
	public String getCp_cont() {
		return cp_cont;
	}
	public void setCp_cont(String cp_cont) {
		this.cp_cont = cp_cont;
	}
	

}
