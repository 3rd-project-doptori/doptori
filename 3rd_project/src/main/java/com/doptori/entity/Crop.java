package com.doptori.entity;

public class Crop {
	
	private	int cp_num;
	private String cp_name;
	private	int cp_type;
	private	String cp_title;
	private	String cp_cont;
	
	
	// getter, setter 만들기
	public int getCp_num() {
		return cp_num;
	}
	public void setCp_num(int cp_num) {
		this.cp_num = cp_num;
	}
	public String getCp_name() {
		return cp_name;
	}
	public void setCp_name(String cp_name) {
		this.cp_name = cp_name;
	}
	public int getCp_type() {
		return cp_type;
	}
	public void setCp_type(int cp_type) {
		this.cp_type = cp_type;
	}
	public String getCp_title() {
		return cp_title;
	}
	public void setCp_title(String cp_title) {
		this.cp_title = cp_title;
	}
	public String getCp_cont() {
		return cp_cont;
	}
	public void setCp_cont(String cp_cont) {
		this.cp_cont = cp_cont;
	}
	

}
