package com.doptori.entity;

public class Chat {

	private	int ch_num;
	private	int ch_cr_num;
	private	int ch_mb_num;
	private	String ch_cont;
	private	String ch_date;
	
	// getter, setter 만들기
	public int getCh_num() {
		return ch_num;
	}
	public void setCh_num(int ch_num) {
		this.ch_num = ch_num;
	}
	public int getCh_cr_num() {
		return ch_cr_num;
	}
	public void setCh_cr_num(int ch_cr_num) {
		this.ch_cr_num = ch_cr_num;
	}
	public int getCh_mb_num() {
		return ch_mb_num;
	}
	public void setCh_mb_num(int ch_mb_num) {
		this.ch_mb_num = ch_mb_num;
	}
	public String getCh_cont() {
		return ch_cont;
	}
	public void setCh_cont(String ch_cont) {
		this.ch_cont = ch_cont;
	}
	public String getCh_date() {
		return ch_date;
	}
	public void setCh_date(String ch_date) {
		this.ch_date = ch_date;
	}	
	
	
}
