package com.doptori.entity;

public class Farm {
	
	private	int fm_num;
	private	int fm_mb_num;
	private	int fm_ad_num;
	private	String fm_name;
	private	String fm_dong;
	private	int fm_size;
	private	String fm_crop;
	
	
	// getter, setter 만들기
	public int getFm_num() {
		return fm_num;
	}
	public void setFm_num(int fm_num) {
		this.fm_num = fm_num;
	}
	public int getFm_mb_num() {
		return fm_mb_num;
	}
	public void setFm_mb_num(int fm_mb_num) {
		this.fm_mb_num = fm_mb_num;
	}
	public int getFm_ad_num() {
		return fm_ad_num;
	}
	public void setFm_ad_num(int fm_ad_num) {
		this.fm_ad_num = fm_ad_num;
	}
	public String getFm_name() {
		return fm_name;
	}
	public void setFm_name(String fm_name) {
		this.fm_name = fm_name;
	}
	public String getFm_dong() {
		return fm_dong;
	}
	public void setFm_dong(String fm_dong) {
		this.fm_dong = fm_dong;
	}
	public int getFm_size() {
		return fm_size;
	}
	public void setFm_size(int fm_size) {
		this.fm_size = fm_size;
	}
	public String getFm_crop() {
		return fm_crop;
	}
	public void setFm_crop(String fm_crop) {
		this.fm_crop = fm_crop;
	}

	
}
